/***********************************************************************************************************************************
 *  Method Name        : dateToString
 *
 *  Method Description : Convert a date time object to a string. Format = DD-MM-YYYY (25-03-2021)
 *                       warning: Char buffer needs to be atleast 10 chars in size.
 *
 *  Entry              : buffer, Char buffer to write to
 *                       bufferSize, Size of char buffer
 *
 *  Exit               : int, Number of bytes written to char buffer.
 *
 **********************************************************************************************************************************/
int date::dateToString(char *buffer, const size_t &bufferSize)
{
    if (bufferSize >= 10) 
    {
        return snprintf(buffer, bufferSize, "%02i-%02i-%04i", day, month, year);
    }
    return 0;
}
