/***********************************************************************************************************************************
 *	Class name         : date
 *
 *  Class Description  : Simple date storage class.
 *
 *	Inheritance        : None
 *
 **********************************************************************************************************************************/
class date 
{
    public:
        uint16_t year = 0;
        uint8_t month = 0;
        uint8_t day = 0;

		/***********************************************************************************************************************************
		 *  Method Name        : date
		 *
		 *  Method Description : Construct a new empty Date object
		 *
		 *  Entry              : None
		 *
		 *  Exit               : None
		 *
		 **********************************************************************************************************************************/
        date(void) {}; //Construct a new empty Date object

		/***********************************************************************************************************************************
		 *  Method Name        : date
		 *
		 *  Method Description : Construct a new Date object.
		 *
		 *  Entry              : year, The year given in YYYY standard so 2021 not only 21.
		 *  					 month, The month given between 1-12.
		 *  					 day, The day given between 1-31.
		 *
		 *  Exit               : None
		 *
		 **********************************************************************************************************************************/
        date(const uint16_t &year, const uint8_t &month, const uint8_t &day) : year(year), month(month), day(day) {};

        int dateToString(char *buffer, const size_t &bufferSize); //Convert a date time object to a string.

		/***********************************************************************************************************************************
		 *  Method Name        : operator ==
		 *
		 *  Method Description : Overloaded operator for equals
		 *
		 *  Entry              : rhs, other object
		 *
		 *  Exit               : true if day, month and year are the same
		 *  					 false if day or month or year differs
		 *
		 **********************************************************************************************************************************/
        bool operator ==(const date &rhs)
        const   {
                    return (rhs.day == day) && (rhs.month == month) && (rhs.year == year);
                }

		/***********************************************************************************************************************************
		 *  Method Name        : operator >
		 *
		 *  Method Description : Overloaded operator for greater then
		 *
		 *  Entry              : rhs, other object
		 *
		 *  Exit               : true, LHS is later then RHS
		 *  					 false, RHS is later then LHS
		 *
		 **********************************************************************************************************************************/
        bool operator >(const date &rhs)
        const   {
                    return (year > rhs.year) ? true : (year == rhs.year && month > rhs.month) ? true : (month == rhs.month && day > rhs.day);
                }
};

